    @Override
    public void setupAnim(T entity, float limbAngle, float limbDistance, float animationProgress, float headYaw, float headPitch) {
        this.modelRoot.resetPose();
        this.body.resetPose();
        this.tail.resetPose();
        float swimTailYaw = sampleSwimTailYaw(animationProgress);
        float swimTailYawDeg = Mth.wrapDegrees(swimTailYaw * Mth.RAD_TO_DEG);
        this.tail.yRot = swimTailYawDeg * Mth.DEG_TO_RAD;
    }

    private static float sampleSwimTailYaw(float animationProgress) {
        float ticks = animationProgress % SWIM_PERIOD_TICKS;
        if (ticks < 0.0F) {
            ticks += SWIM_PERIOD_TICKS;
        }
        float t = ticks / 20.0F;
        for (int i = 0; i < SWIM_KEYFRAME_TIMES.length - 1; i++) {
            float t0 = SWIM_KEYFRAME_TIMES[i];
            float t1 = SWIM_KEYFRAME_TIMES[i + 1];
            if (t <= t1) {
                float alpha = (t - t0) / (t1 - t0);
                float y0 = SWIM_KEYFRAME_YAWS_DEG[i];
                float y1 = SWIM_KEYFRAME_YAWS_DEG[i + 1];
                return Mth.DEG_TO_RAD * Mth.lerp(alpha, y0, y1);
            }
        }
        float last = SWIM_KEYFRAME_YAWS_DEG[SWIM_KEYFRAME_YAWS_DEG.length - 1];
        return Mth.DEG_TO_RAD * last;
    }

